import Toast from '@/components/Toast.vue'
import { useUserStore } from "@/stores/user";
import axios from "axios";

export default {
  setup(){
    const userStore = useUserStore()
    console.log("supersuerCONSOLELOG", userStore.user.superuser)
    return{
      userStore
    }
  },
  data(){
    return{
      countFavourite:0,
      countBasket:0,
    }
  },
  mounted(){
      if(this.userStore.user && this.userStore.user.superuser !== true){
        this.getBaskets()
        this.getFavourites()
    }
  },
  components: {
    Toast
  },
  methods: {
    onContextMenu(e) {
    //prevent the browser's default menu
    e.preventDefault();
    //show our menu
    this.$contextmenu({
      x: e.x,
      y: e.y,
      items: [
        {
          label: "Перейти в профиль",
          onClick: () => {
            this.$router.push(`/profile/${this.userStore.user.id}/`)

    console.log("supersuerCONSOLELOG", this.userStore.user.superuser)
          }
        },
        {
          label: "Действия по профилю",
          children: [
            { label: "Выйти из аккаунта",
          onClick: () => {
            this.logout()
          } },
          ]
        },
      ]
    });
  },
          logout() {
            console.log('Log out')
            this.userStore.removeToken()
            this.$router.push('/signin')
        },

    getFavourites(){
      axios
                          .post(`/api/favourite/get_count_favourite/`, {"user": this.userStore.user.id})
                          .then(response => {
                             this.countFavourite = response.data.count
                          })
                          .catch(error => {
                              console.log('error', error)
                          })
    },
    getBaskets(){
        axios
                          .post(`/api/basket/get_count_basket/`, {"user": this.userStore.user.id})
                          .then(response => {
                             this.countBasket = response.data.count
                          })
                          .catch(error => {
                              console.log('error', error)
                          })
    },
    },

  beforeCreate() {
    this.userStore.initStore()

    const token = this.userStore.user.access

    if(token){
      axios.defaults.headers.common["Authorization"] = "Bearer " + token
    }
    else {
      axios.defaults.headers.common["Authorization"] = ""

    }
  },


}
